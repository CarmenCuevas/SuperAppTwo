public class Local {

    private String localName;
    private String localDirec;
    private String localTel;
    private String localId;
    private float localLat;
    private float localLong;

    public String getLocalName() {
        return localName;
    }

    public void setLocalName(String localName) {
        this.localName = localName;
    }

    public String getLocalDirec() {
        return localDirec;
    }

    public void setLocalDirec(String localDirec) {
        this.localDirec = localDirec;
    }

    public String getLocalTel() {
        return localTel;
    }

    public void setLocalTel(String localTel) {
        this.localTel = localTel;
    }

    public String getLocalId() {
        return localId;
    }

    public void setLocalId(String localId) {
        this.localId = localId;
    }

    public float getLocalLat() {
        return localLat;
    }

    public void setLocalLat(float localLat) {
        this.localLat = localLat;
    }

    public float getLocalLong() {
        return localLong;
    }

    public void setLocalLong(float localLong) {
        this.localLong = localLong;
    }
}
